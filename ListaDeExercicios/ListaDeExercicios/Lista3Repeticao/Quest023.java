//-Escreva um programa que exiba todos os números pares de 1 a 100, utilizando
//while.




public class Quest023 {
    public static void main(String[] args) {
        int contador = 2;

        while (contador <= 100) {
            System.out.println(contador);
            contador += 2;  // como e de 1 a 100 n coloquei o 0 na impressão
        }
    }
}
